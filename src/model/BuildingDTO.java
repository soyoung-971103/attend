package model;

public class BuildingDTO {

	int id;
	String name;
	byte floor;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public byte getFloor() {
		return floor;
	}
	public void setFloor(byte floor) {
		this.floor = floor;
	}	

}
